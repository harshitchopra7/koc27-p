a = float(input("Please enter number 1 "))
b = float(input("Please enter number 2 "))
operator=(input("enter the operator: "))
if operator== "+":
    print((a)+(b))
elif operator=="-":
    print((a)-(b))
elif operator=="*":
    print((a)*(b))
elif operator=="%":
    print((a)%(b))
elif operator=="**":
    print((a)**(b))
elif operator=="/":
    print((a)/(b))
elif operator=="//":
    print((a)//(b))
else:
    print("invalid input")
